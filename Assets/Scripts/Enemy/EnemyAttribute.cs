public class EnemyAttribute
{
    public float 生命值;
    public float 攻击力;
    public float 防御;
    public float 移动速度;
    public float 经验值;

    public EnemyAttribute(float 生命值, float 攻击力, float 防御, float 移动速度, float 经验值)
    {
        this.生命值 = 生命值;
        this.攻击力 = 攻击力;
        this.防御 = 防御;
        this.移动速度 = 移动速度;
        this.经验值 = 经验值;
    }

    public EnemyAttribute()
    {

    }
}